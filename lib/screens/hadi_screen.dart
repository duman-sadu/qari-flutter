class HadiScreen extends StatefulWidget {
  final String? initialQuery;
  const HadiScreen({super.key, this.initialQuery});

  @override
  State<HadiScreen> createState() => _HadiScreenState();
}

class _HadiScreenState extends State<HadiScreen>
    with SingleTickerProviderStateMixin {
  LanguageProvider get _s => context.read<LanguageProvider>();

  late final TabController _tabCtrl;
  final _msgCtrl = TextEditingController();
  final _scrollCtrl = ScrollController();
  final _apiKeyCtrl = TextEditingController();

  final List<HadiMessage> _history = [];
  bool _sending = false;
  bool _showApiSetup = false;

  bool _showDailyAyah = true;
  bool _showQuestions = true;
  int _quickCat = 0;
  bool _chatListening = false;

  // Knowledge quiz state
  bool _kqMode = false;
  bool _kqStarted = false;
  int _kqIdx = 0;
  String? _kqSelected;
  int _kqScore = 0;
  late List<_KQ> _kqShuffled;

  // Challenge state
  String? _challengeId;
  bool _isChallenge = false;
  bool _challengeCreating = false;
  String _challengeOpponentName = '';
  int _challengeMyScore = -1;
  int _challengeOpponentScore = -1;
  StreamSubscription<DocumentSnapshot<Map<String, dynamic>>>? _challengeSub;

  // Incoming challenges (sent to me by friends)
  List<Map<String, dynamic>> _incomingChallenges = [];
  StreamSubscription<List<Map<String, dynamic>>>? _incomingChallengesSub;

  // Quiz state
  Map<String, dynamic>? _quizAyah;
  bool _quizRevealed = false;
  String _quizHadiQ = '';
  bool _quizLoading = false;

  // Pronunciation check state
  final SpeechToText _stt = SpeechToText();
  bool _sttAvailable = false;
  bool _listening = false;
  String _heardText = '';
  bool _checkingPronunciation = false;
  String _pronunciationResult = '';
  bool _pronunciationPending = false; // prevents double _checkPronunciation call

  @override
  void initState() {
    super.initState();
    _tabCtrl = TabController(length: 3, vsync: this);
    _kqShuffled = (List<_KQ>.of(_kqList)..shuffle()).take(23).toList();
    _loadApiKey();
    _initStt();
    if (widget.initialQuery != null && widget.initialQuery!.isNotEmpty) {
      WidgetsBinding.instance.addPostFrameCallback(
          (_) => _send(widget.initialQuery));
    }
    WidgetsBinding.instance.addPostFrameCallback((_) {
      if (mounted) _addGreeting();
      _subscribePendingChallenges();
    });
  }

  void _subscribePendingChallenges() {
    _incomingChallengesSub?.cancel();
    final uid = FirebaseAuth.instance.currentUser?.uid ?? '';
    if (uid.isEmpty) return;
    _incomingChallengesSub = ChallengeService.pendingForMeStream().listen(
      (list) { if (mounted) setState(() => _incomingChallenges = list); },
      onError: (_) {},
    );
  }

  @override
  void dispose() {
    _challengeSub?.cancel();
    _incomingChallengesSub?.cancel();
    _stt.stop();
    _tabCtrl.dispose();
    _msgCtrl.dispose();
    _scrollCtrl.dispose();
    _apiKeyCtrl.dispose();
    super.dispose();
  }

  Future<void> _loadApiKey() async {
    final key = await HadiService.getApiKey();
    if (key != null) _apiKeyCtrl.text = key;
  }

  void _addGreeting() {
    final name = context.read<OnboardingProvider>().firstName;
    final text = _s.greetingMsg(name);
    setState(() {
      _history.add(HadiMessage(role: 'assistant', text: text, time: DateTime.now()));
    });
  }

  HadiContext _buildContext() {
    final plan = context.read<PlanProvider>();
    final onboarding = context.read<OnboardingProvider>();
    final today = DateTime.now().toString().substring(0, 10);
    final learnedToday = plan.learnedHistory
        .where((h) => h.date == today)
        .fold(0, (s, h) => s + h.count);
    final totalLearned = plan.learnedHistory.fold(0, (s, h) => s + h.count);
    final totalRead = plan.readHistory.fold(0, (s, h) => s + h.count);
    final studiedToday = learnedToday > 0 ||
        plan.readHistory.any((h) => h.date == today && h.count > 0);

    return HadiContext(
      streak: plan.streak,
      totalLearned: totalLearned,
      totalRead: totalRead,
      studyMode: plan.studyMode,
      userName: onboarding.firstName,
      studiedToday: studiedToday,
      learnedToday: learnedToday,
    );
  }

  Future<void> _send([String? quick]) async {
    final text = quick ?? _msgCtrl.text.trim();
    if (text.isEmpty || _sending) return;
    _msgCtrl.clear();
    setState(() {
      _history.add(HadiMessage(role: 'user', text: text, time: DateTime.now()));
      _sending = true;
    });
    _scrollDown();

    final reply = await HadiService.send(
      userMessage: text,
      context: _buildContext(),
      history: _history.where((m) => m.role != 'assistant' || _history.indexOf(m) > 0).toList(),
      isRu: _s.isRu,
    );

    if (mounted) {
      setState(() {
        _history.add(HadiMessage(role: 'assistant', text: reply, time: DateTime.now()));
        _sending = false;
      });
      _scrollDown();
    }
  }

  void _scrollDown() {
    Future.delayed(const Duration(milliseconds: 100), () {
      if (_scrollCtrl.hasClients) {
        _scrollCtrl.animateTo(
          _scrollCtrl.position.maxScrollExtent,
          duration: const Duration(milliseconds: 300),
          curve: Curves.easeOut,
        );
      }
    });
  }

  void _newQuiz() {
    final plan = context.read<PlanProvider>();
    final keys = plan.learnedAyahKeys.toList();
    if (keys.isEmpty) {
      setState(() {
        _quizAyah = null;
        _quizHadiQ = _s.noLearnedAyahs();
        _quizRevealed = false;
      });
      return;
    }
    final key = keys[Random().nextInt(keys.length)];
    final parts = key.split(':');
    final ch = int.tryParse(parts[0]) ?? 1;
    final v = int.tryParse(parts.length > 1 ? parts[1] : '1') ?? 1;
    final name = (_s.isRu ? surahNamesRu : surahNames)[(ch - 1).clamp(0, 113)];
    setState(() {
      _quizAyah = {'chapter': ch, 'verse': v, 'surah': name, 'key': key};
      _quizRevealed = false;
      _quizHadiQ = '';
      _quizLoading = true;
      _resetPronunciation();
    });
    _loadQuizQuestion(ch, v, name);
    _loadQuizAyahData(ch, v);
  }

  Future<void> _loadQuizQuestion(int ch, int v, String name) async {
    final q = await HadiService.quizQuestion(_buildContext(), name, v, '$ch:$v', isRu: _s.isRu);
    if (mounted) setState(() { _quizHadiQ = q; _quizLoading = false; });
  }

  Future<void> _loadQuizAyahData(int ch, int v) async {
    final tid = _s.translationId;
    final data = await fetchAyah(ch, v, tid);
    if (mounted && data != null && _quizAyah != null) {
      setState(() => _quizAyah = {
        ..._quizAyah!,
        ...data,
        'surah': _quizAyah!['surah'], // сохраняем казахское название
      });
    }
  }

  // ── STT / pronunciation ───────────────────────────────────────────────────

  Future<void> _initStt() async {
    final available = await _stt.initialize(
      onError: (e) => debugPrint('STT: $e'),
    );
    if (mounted) setState(() => _sttAvailable = available);
