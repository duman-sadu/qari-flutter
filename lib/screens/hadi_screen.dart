        .where((h) => h.date == today)
        .fold(0, (s, h) => s + h.count);
    final readToday = plan.readHistory
        .where((h) => h.date == today)
        .fold(0, (s, h) => s + h.count);
    final totalLearned = plan.learnedHistory.fold(0, (s, h) => s + h.count);
