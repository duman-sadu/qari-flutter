  Future<void> _showFriendsSheet() async {
    if (_challengeCreating) return;
    setState(() => _challengeCreating = true);

    List<FriendProfile> friends;
    try {
      friends = await FriendService.getFriends();
    } catch (_) {
      friends = [];
    }
    if (!mounted) return;
    setState(() => _challengeCreating = false);

    // No friends: create a code-based challenge so the button always does something
    if (friends.isEmpty) {
      await _createChallengeForFriend(null);
      return;
    }

    final c = AppColors.of(context);
    await showModalBottomSheet(
      context: context,
      useRootNavigator: true,
      backgroundColor: c.card,
      shape: const RoundedRectangleBorder(
          borderRadius: BorderRadius.vertical(top: Radius.circular(20))),
      builder: (sheetCtx) => _FriendsSheet(
        friends: friends,
        isRu: _s.isRu,
        c: c,
        sheetContext: sheetCtx,
        onChallenge: _createChallengeForFriend,
      ),
    );
  }

  Future<void> _createChallengeForFriend(FriendProfile? friend) async {
    setState(() => _challengeCreating = true);
    try {
      final onboarding = context.read<OnboardingProvider>();
      final name = '${onboarding.firstName} ${onboarding.lastName}'.trim();
      final allIdx = List.generate(_kqList.length, (i) => i)..shuffle();
      final indices = allIdx.take(23).toList();
      final id = await ChallengeService.create(
        questionIndices: indices,
        creatorName: name.isEmpty ? 'Қолданушы' : name,
        isRu: _s.isRu,
        targetUid: friend?.uid,
        targetName: friend?.name,
      );
      if (friend != null) {
        ApiService.sendChallengeNotification(
          targetUid: friend.uid,
          senderName: name.isEmpty ? 'Қолданушы' : name,
          challengeId: id,
          isRu: _s.isRu,
        );
      }
      if (!mounted) return;
      setState(() {
        _challengeId = id;
        _challengeOpponentName = friend?.name ?? '';
        _isChallenge = true;
        _challengeCreating = false;
        _kqShuffled = indices.map((i) => _kqList[i]).toList();
        _kqIdx = 0;
        _kqSelected = null;
        _kqScore = 0;
        _kqStarted = true;
      });
    } catch (e) {
      if (!mounted) return;
      setState(() => _challengeCreating = false);
      ScaffoldMessenger.of(context).showSnackBar(SnackBar(
        content: Text(_s.isRu ? 'Не удалось создать вызов' : 'Шақыру жасалмады'),
      ));
    }
  }

  Future<void> _joinChallenge(String code) async {
    try {
      final data = await ChallengeService.get(code);
      if (!mounted) return;
      if (data == null) {
        ScaffoldMessenger.of(context).showSnackBar(SnackBar(
          content: Text(_s.isRu ? 'Код не найден' : 'Код табылмады'),
        ));
        return;
      }
      final indices = List<int>.from(data['questionIndices'] as List);
      setState(() {
        _challengeId = code;
        _isChallenge = true;
        _challengeOpponentName = ChallengeService.opponentName(data);
        _challengeOpponentScore = ChallengeService.opponentScore(data);
        _kqShuffled = indices.map((i) => _kqList[i]).toList();
        _kqIdx = 0;
        _kqSelected = null;
        _kqScore = 0;
        _kqStarted = true;
      });
    } catch (_) {
      if (mounted) {
        ScaffoldMessenger.of(context).showSnackBar(SnackBar(
          content: Text(_s.isRu ? 'Ошибка загрузки' : 'Жүктеу қатесі'),
        ));
      }
    }
  }

  Future<void> _onChallengeFinished() async {
    final id = _challengeId;
    if (id == null) return;
    final score = _kqScore;
    setState(() => _challengeMyScore = score);
    final onboarding = context.read<OnboardingProvider>();
    final name = '${onboarding.firstName} ${onboarding.lastName}'.trim();
    try {
      await ChallengeService.saveScore(
        challengeId: id,
        score: score,
        playerName: name.isEmpty ? 'Қолданушы' : name,
      );
    } catch (_) {}
    _challengeSub?.cancel();
    _challengeSub = ChallengeService.watch(id).listen((snap) {
      if (!mounted || !snap.exists) return;
      final data = snap.data()!;
      final opScore = ChallengeService.opponentScore(data);
      final opName  = ChallengeService.opponentName(data);
      setState(() {
        _challengeOpponentScore = opScore;
        if (opName.isNotEmpty) _challengeOpponentName = opName;
      });
    });
  }

  Widget _buildChallengeResult(AppColors c) {
    final total  = _kqShuffled.length;
    final myScore = _challengeMyScore >= 0 ? _challengeMyScore : _kqScore;
    final opScore = _challengeOpponentScore;
    final waiting = opScore < 0;
    final opName  = _challengeOpponentName.isNotEmpty
        ? _challengeOpponentName
        : (_s.isRu ? 'Соперник' : 'Қарсылас');
