  void _resetPronunciation() {
    _heardText = '';
    _pronunciationResult = '';
    _checkingPronunciation = false;
    _pronunciationPending = false;
    if (_listening) { _stt.stop(); _listening = false; }
  }

  Future<void> _startListening() async {
    if (!_sttAvailable || _listening) return;
    setState(() { _heardText = ''; _pronunciationResult = ''; _listening = true; _pronunciationPending = false; });

    // Find best Arabic locale available on device
    final locales = await _stt.locales();
    final arLocale = locales
        .firstWhere((l) => l.localeId.startsWith('ar'),
            orElse: () => locales.first)
        .localeId;

    await _stt.listen(
      onResult: (result) {
        if (!mounted) return;
        setState(() => _heardText = result.recognizedWords);
        if (result.finalResult && !_pronunciationPending) {
          _pronunciationPending = true;
          setState(() => _listening = false);
          _checkPronunciation();
        }
      },
      localeId: arLocale,
      listenFor: const Duration(seconds: 25),
      pauseFor: const Duration(seconds: 3),
    );
  }

  Future<void> _stopListening() async {
    await _stt.stop();
    if (!mounted) return;
    setState(() => _listening = false);
    // onResult(finalResult) may already have fired when stt.stop() was called;
    // only call check here if it hasn't been triggered yet.
    if (!_pronunciationPending) {
      _pronunciationPending = true;
      _checkPronunciation();
    }
  }

  // ── Chat voice input ───────────────────────────────────────────────────────

  Future<void> _startChatListening() async {
    if (!_sttAvailable || _chatListening) return;
    await _stt.stop();
    setState(() { _chatListening = true; _listening = false; });

    final locales = await _stt.locales();
    final locale = locales
        .firstWhere((l) => l.localeId.startsWith('kk'),
            orElse: () => locales.firstWhere(
                  (l) => l.localeId.startsWith('ru'),
                  orElse: () => locales.first,
                ))
        .localeId;

    await _stt.listen(
      onResult: (result) {
        if (!mounted) return;
        _msgCtrl.value = TextEditingValue(
          text: result.recognizedWords,
          selection: TextSelection.collapsed(
              offset: result.recognizedWords.length),
        );
        if (result.finalResult) {
          setState(() => _chatListening = false);
        }
      },
      localeId: locale,
      listenFor: const Duration(seconds: 30),
      pauseFor: const Duration(seconds: 3),
    );
  }

  Future<void> _stopChatListening() async {
    await _stt.stop();
    if (mounted) setState(() => _chatListening = false);
  }

  Future<void> _checkPronunciation() async {
    final arabic = (_quizAyah?['arabic'] ?? '').toString();
    final surah  = (_quizAyah?['surah']   ?? '').toString();
    final verse  = _quizAyah?['verse']?.toString() ?? '';
    if (arabic.isEmpty) return;

    setState(() { _checkingPronunciation = true; _pronunciationResult = ''; });

    final isRu = _s.isRu;
    final prompt = _heardText.isEmpty
        ? (isRu
            ? 'Пользователь читал $verse-й аят суры $surah, но голос не распознан. Подбодри повторить.'
            : 'Пайдаланушы $surah сүресінің $verse-аятын оқыды, бірақ дыбыс танылмады. Қайталап оқыуға ынталандыр.')
        : (isRu
            ? 'Пользователь прочитал: "$_heardText".\n'
              'Правильный аят: "$arabic" ($surah, $verse-й аят).\n'
              'Оцени в 2–3 предложениях: правильно ли? Если есть ошибки — на какие слова обратить внимание. Отвечай тепло и ободряюще.'
            : 'Пайдаланушы мына аятты оқыды: "$_heardText".\n'
              'Дұрыс аят: "$arabic" ($surah, $verse-аят).\n'
              'Тыңдап, 2-3 сөйлемде бағала: дұрыс па? '
              'Қателер болса, қай сөздерге назар аудару керек. '
              'Жылы және ынталандырушы тонда жауап бер.');

    final result = await HadiService.send(
      userMessage: prompt,
      context: _buildContext(),
      history: [],
      isRu: isRu,
    );
    if (mounted) setState(() { _pronunciationResult = result; _checkingPronunciation = false; });
  }

