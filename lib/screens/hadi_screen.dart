class _RecordingWave extends StatefulWidget {
  final Color color;
  const _RecordingWave({required this.color});

  @override
  State<_RecordingWave> createState() => _RecordingWaveState();
}

class _RecordingWaveState extends State<_RecordingWave>
    with SingleTickerProviderStateMixin {
  late final AnimationController _ctrl;

  @override
  void initState() {
    super.initState();
    _ctrl = AnimationController(
      vsync: this,
      duration: const Duration(milliseconds: 900),
    )..repeat(reverse: true);
  }

  @override
  void dispose() {
    _ctrl.dispose();
    super.dispose();
  }

  @override
  Widget build(BuildContext context) {
    final s = context.watch<LanguageProvider>();
    return Row(
      mainAxisAlignment: MainAxisAlignment.center,
      children: [
        Icon(Icons.mic_rounded, color: widget.color, size: 20),
        const SizedBox(width: 10),
        ...List.generate(5, (i) {
          final delay = i * 0.18;
          return AnimatedBuilder(
            animation: _ctrl,
            builder: (_, child) {
              final t = ((_ctrl.value + delay) % 1.0);
              final h = 6.0 + (sin(t * pi) * 16.0);
              return Container(
                margin: const EdgeInsets.symmetric(horizontal: 2),
                width: 4,
                height: h,
                decoration: BoxDecoration(
                  color: widget.color.withValues(alpha: 0.7 + t * 0.3),
                  borderRadius: BorderRadius.circular(4),
                ),
              );
            },
          );
        }),
        const SizedBox(width: 10),
        Text(s.tr('listening'),
            style: TextStyle(
                fontSize: 13,
                fontWeight: FontWeight.w600,
                color: widget.color)),
      ],
    );
  }
}

