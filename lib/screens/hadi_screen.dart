  // ── Knowledge Quiz ────────────────────────────────────────────────────────
  void _kqStart() {
    _challengeSub?.cancel();
    _challengeSub = null;
    setState(() {
      _challengeId = null;
      _isChallenge = false;
      _challengeCreating = false;
      _challengeOpponentName = '';
      _challengeMyScore = -1;
      _challengeOpponentScore = -1;
      _kqShuffled = (List<_KQ>.of(_kqList)..shuffle()).take(23).toList();
      _kqIdx = 0;
      _kqSelected = null;
      _kqScore = 0;
      _kqStarted = false;
    });
  }

  void _kqAnswer(String option) {
    if (_kqSelected != null) return;
    final kq = _kqShuffled[_kqIdx];
    final correctAnswer = (_s.isRu && kq.correctRu != null) ? kq.correctRu! : kq.correct;
    setState(() {
      _kqSelected = option;
      if (correctAnswer == option) _kqScore++;
    });
  }

  void _kqNext() {
    if (_kqIdx + 1 >= _kqShuffled.length) {
      setState(() => _kqIdx = _kqShuffled.length);
      if (_isChallenge) _onChallengeFinished();
    } else {
      setState(() { _kqIdx++; _kqSelected = null; });
    }
  }

