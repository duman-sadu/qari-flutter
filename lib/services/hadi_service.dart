  static String? _detectIntent(String t, String? surah, String? prophet) {
    bool has(String kw) => t.contains(kw);
    bool anyOf(List<String> kws) => kws.any(has);

    // Prophet-specific queries
    if (prophet != null) {
      return 'prophet_info';
    }

    // Surah-specific queries (entity present)
    if (surah != null) {
      if (anyOf(['туралы', 'жайлы', 'айт', 'не ерекше', 'атауы', 'аталады'])) {
        return 'surah_info';
      }
      // Default for surah mention = ayah count
      return 'surah_ayah_count';
    }

    // Total counts
    if (has('сүре') && anyOf(['неше', 'санын', 'саны'])) return 'count_surah';
    if (has('аят') && anyOf(['неше', 'санын', 'саны']) && !has('сүресінде')) {
      return 'count_ayah_total';
    }
    if (has('жуз') && anyOf(['неше', 'бөлін'])) return 'count_juz';
    if (has('хизб') && anyOf(['неше', 'бөлін'])) return 'count_hizb';

    // Revelation — years
    if ((has('жылда') || anyOf(['неше жыл', 'қанша жыл'])) &&
        anyOf(['түсті', 'түскен', 'жіберілген'])) {
      return 'years_revealed';
    }
    // Revelation — place
    if (anyOf(['қай жерде', 'қайда ', 'жерде түс']) ||
        (anyOf(['мекке', 'мәдина']) && anyOf(['неше', 'жыл', 'түс']))) {
      return 'where_revealed';
    }

    // First / last (revelation)
    if (has('алғашқы') && has('аят')) return 'first_ayah_revealed';
    if (has('алғашқы') && has('сүре') && has('түскен')) return 'first_surah_revealed';
    if (has('соңғы')   && has('сүре') && has('түскен')) return 'last_surah_revealed';

    // Longest / shortest
    if (has('ең ұзын') && has('сүре')) return 'longest_surah';
    if (has('ең қысқа') && has('сүре')) return 'shortest_surah';
    if (has('ең ұзын') && has('аят')) return 'longest_ayah';

    // First / last in mushaf order
    if (anyOf(['бірінші', 'алғашқы']) && has('сүре') && !has('түскен')) {
      return 'first_surah_mushaf';
    }
    if (has('соңғы') && has('сүре') && !has('түскен')) return 'last_surah_mushaf';

    // Special
    if (has('пайғамбар') && anyOf(['ең көп', 'жиі', 'аталған', 'атал'])) return 'prophet_most';
    if (has('пайғамбар') && anyOf(['неше', 'санын', 'саны', 'қанша'])) return 'count_prophets';
    if (has('пайғамбар') && anyOf(['тізім', 'аттары', 'кімдер', 'барлық', 'бәрі'])) {
      return 'list_prophets';
    }
    if (has('алғашқы') && has('пайғамбар')) return 'first_prophet';
    if (has('соңғы') && has('пайғамбар')) return 'last_prophet';
    if (anyOf(['қадір түні', 'лайлатул', 'қадр түні'])) return 'laylatul_qadr';
    if (has('әйел') && anyOf(['есімімен', 'атымен', 'аталған'])) return 'woman_surah';
    if (has('фатиха') && anyOf(['анасы', 'ерекше', 'басқа атауы'])) return 'fatiha_alias';

    return null;
  }

