  static String? _nluReply(String raw, HadiContext ctx) {
    final t = _normalize(raw);

    // Du'a lookups — highest priority
    final dua = _findDua(t);
    if (dua != null) return dua;

    // Thematic verse lookups
    final verse = _findVerse(t);
    if (verse != null) return verse;

    // Hadith lookups
    final hadith = _findHadith(t);
    if (hadith != null) return hadith;

    // Topic Q&A (afterlife, science, surah descriptions)
    final qa = _findQA(t);
    if (qa != null) return qa;

    final prophet = _findProphet(t);
    final surah = prophet == null ? _findSurah(t) : null;
    final intent = _detectIntent(t, surah, prophet);
    if (intent == null) return null;
    final ans = _getAnswer(intent, surah, prophet: prophet);
    return ans.isEmpty ? null : ans;
  }

