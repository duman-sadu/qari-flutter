  static String _normalize(String text) {
    var t = text.toLowerCase().trim();
    t = t.replaceAll('«', '').replaceAll('»', '');
    t = t.replaceAll(RegExp(r'[?.!,;:]+'), ' ');
    return t
        .replaceAll('қанша', 'неше')
        .replaceAll('қандай', 'қайсы')
        .replaceAll('баккара', 'бақара')
        .replaceAll('бакара', 'бақара')
        .replaceAll('ихлас', 'ықылас')
        .replaceAll('ихляс', 'ықылас')
        .replaceAll('аль-', 'әл-')
        .replaceAll('ан-', 'ән-')
        .replaceAll('йасин', 'ясин')
        .replaceAll('аср', 'асыр')
        .replaceAll('насыр', 'наср');
  }

