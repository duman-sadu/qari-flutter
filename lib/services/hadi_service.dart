    if (_has(msg, ['рамадан', 'пост', 'ураза'])) {
      return 'Рамадан — месяц Корана! '
          'В этом месяце был ниспослан Коран, и награда за чтение умножается. '
          'Старайтесь слушать Коран полностью на молитве таравих! 🌙';
    }

    // Спасибо / похвала
    if (_has(msg, ['спасибо', 'хорошо', 'отлично', 'маша', 'баракалла', 'джазак'])) {
      return 'БаракаллахуфикА, $name! 🐪 '
          'Да примет Аллах ваше чтение и ниспошлёт баракят!';
    }

    // Квиз
    if (_has(msg, ['квиз', 'тест', 'проверить', 'испытание', 'знаю ли'])) {
      return 'Перейдите в раздел «Квиз»! 🎯 '
          'Там случайные вопросы по заученным аятам. '
          'Можно проверить произношение вслух!';
    }

    // Интересные факты
    if (_has(msg, ['интересный', 'факт', 'не знал', 'удивительно', 'сколько сур', 'сколько аятов'])) {
      return randomFact(isRu: true);
    }

    // Дефолтный ответ
    final r = Random();
    if (r.nextInt(4) == 0) {
      return '${randomFact(isRu: true)}\n\nДа благословит Аллах, $name! 🐪';
    }
    final defaults = [
      'МашааАллах, $name! Читайте постоянно — да ниспошлёт Аллах баракят! 🐪',
      'БаракаллахуфикА! Проверьте знания в разделе «Квиз»! 🎯',
      'Да примет Аллах ваше чтение! Один аят в день — 365 аятов в год! ✨',
      '${ctx.streak > 0 ? "Ваша серия — ${ctx.streak} дней" : "Начните сегодня"} — продолжайте! 💪',
      'Постоянство в заучивании — самое важное. БаракаллахуфикА! 🌙',
    ];
    return defaults[r.nextInt(defaults.length)];
  }

  static bool _has(String msg, List<String> keywords) =>
      keywords.any((k) => msg.contains(k));

  // ── Quiz question (локальная версия) ──────────────────────────────────────

  static String _localQuizQuestion(String surahName, int verse, {bool isRu = false}) {
    if (isRu) {
      final questions = [
        'Прочитайте $verse-й аят суры $surahName 🎤',
        'Помните? $surahName — $verse-й аят! Произнесите вслух 📖',
        'Знаете $verse-й аят наизусть? Прочитайте! ($surahName) ✨',
        'Квиз: назовите $verse-й аят суры $surahName 🎯',
      ];
      return questions[verse % questions.length];
    }
    final questions = [
      '$surahName сүресінің $verse-аятын оқып шығыңыз 🎤',
      'Есіңізде бар ма? $surahName — $verse-аят! Дауыстап айтып беріңіз 📖',
      '$verse-аятты жатқа білесіз бе? Оқып шығыңыз! ($surahName) ✨',
      'Квиз: $surahName сүресінің $verse-аятын айтып беріңіз 🎯',
    ];
    return questions[verse % questions.length];
  }

  // ── Public methods ─────────────────────────────────────────────────────────

  static Future<String> quickMotivation(HadiContext ctx, {bool isRu = false}) async {
    return send(
      userMessage: isRu ? 'мотивация' : 'мотивация',
      context: ctx,
      history: [],
      isRu: isRu,
    );
  }

  static Future<String> quizQuestion(HadiContext ctx, String surahName,
      int verse, String arabic, {bool isRu = false}) async {
    final apiKey = await getApiKey();
    if (apiKey == null || apiKey.isEmpty) {
      return _localQuizQuestion(surahName, verse, isRu: isRu);
    }
    final prompt = isRu
        ? 'Составь вопрос для квиза: проверь, знает ли пользователь $verse-й аят '
          'суры "$surahName" ($arabic) наизусть. Задай только один конкретный вопрос.'
        : 'Квиз сұрағы жасашы: "$surahName" сүресінің $verse-аятын '
          '($arabic) пайдаланушы жатқа білетінін тексер. '
          'Тек бір ғана нақты сұрақ қой.';
    return send(
      userMessage: prompt,
      context: ctx,
      history: [],
      isRu: isRu,
    );
  }
